      $nameErr = "Only letters and white space allowed";
    }
  }

  if (empty($_POST["reg_no"])) {
    $genderErr = "reg no is required";
  } else {
    $gender = $_POST["reg_no"];
  }
}
  
  if (empty($_POST["email"])) {
    $emailErr = "Email is required";
  } else {
    $email = $_POST["email"];

